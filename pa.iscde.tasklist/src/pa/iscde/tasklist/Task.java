package pa.iscde.tasklist;

public class Task {

	private String token;
	private String description;
	private String project;
	private String file;
	private int line;

	public String getToken() {
		return token;
	}

	public String getDescription() {
		return description;
	}

	public String getProject() {
		return project;
	}

	public String getFile() {
		return file;
	}

	public int getLine() {
		return line;
	}

}