package pa.iscde.tasklist;

public class Task {

	private String token;
	private String project;
	private String file;
	private int line;
	
	public String getToken() {
		return token;
	}

	public String getProject() {
		return project;
	}

	public String getFile() {
		return file;
	}
	
	public int getLine() {
		return line;
	}
	

}